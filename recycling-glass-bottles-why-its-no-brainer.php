              <img src="assets/img/recycling_bottle_glass.webp" alt="Blog">
                <p>Take a moment to think about your day – from the glass of water in the morning to the soda bottle at lunch or the glass of juice you finish at dinner. Glass is all around us. It’s strong, safe, versatile, and trusted for packaging food, drinks, and medicine. In short, glass is part of our everyday life.</p>
                <p>But here’s the catch: once we’re done with it, most glass in Kenya ends up in landfills, 
                    dumpsites, or worse – littering our environment. Unlike organic waste, glass doesn’t 
                    decompose. A bottle tossed away today will still be sitting around hundreds of years from now.</p>
                <p>In landfills such as Dandora, it takes up valuable space, and in open environments, it becomes both an eyesore and a hazard.</p>

              <p>The challenge in Kenya is two-fold: first, the lack of structured systems to collect and recycle glass at scale, and second, the dangers it poses to waste pickers and communities living near dumpsites. 
                  Broken glass is not just another piece of trash – it cuts, injures, and exposes waste handlers to infections. For many vulnerable communities, especially children who scavenge around dumpsites, glass waste is a daily threat.</p>

              <p>   So, why should we recycle glass? The reasons are plenty:</p>
              <ul>
                  <li>Endless recyclability: Glass is one of the few materials that can be recycled endlessly without losing quality.</li>
                  <li>Energy savings: Recycling just one glass bottle saves enough energy to power a light bulb for four hours.
                      The more we recycle, the less energy we use in producing new glass.
                  </li>
                  <li>Pollution reduction: Using recycled glass cuts down greenhouse gas emissions significantly, 
                      meaning cleaner air for us all.
                  </li>
                  <li>Safer environments: Keeping glass out of dumpsites protects waste pickers and nearby communities from cuts and health risks.
                  </li>
                  <li>Income opportunities: Glass recycling creates a livelihood for waste pickers and contributes to the circular economy. What we see as “waste” can actually put money into someone’s pocket.
                  </li>
              </ul>
              <figure>
                  <img src="assets/img/article2.png" class="mx-auto" alt="Article 2" />
                  <figcaption>Yajayo works with hundreds of waste pickers in reclaiming bottles for recycling</figcaption>
              </figure>
              
              
           <p>At Yajayo, we’ve seen this potential first-hand. Through our reverse logistics systems, we’ve helped collect and channel glass back into reuse and recycling streams. By linking producers, businesses, and recyclers, we make it easier for glass to find its way back into the production cycle rather than ending up as waste. It’s proof that when the right structures are in place, recycling works – and everyone wins.</p>

           <p> The good news is that the solution is literally in our hands. Every time you finish a bottle or a glass, you’re making a choice. If you’re a business, you can partner with organizations like Yajayo to take responsibility for the glass packaging you put out. If you’re an individual, you can make the simple switch of separating your glass waste for recycling.</p>
           <p>Recycling glass is one of those rare “no-brainer” actions. It saves energy, reduces pollution, protects communities, and even creates jobs. So, let’s stop treating glass as waste and start seeing it for what it truly is – a valuable resource with infinite lives.</p>
            <strong>Your empty bottle today could be someone’s opportunity tomorrow. Let’s recycle it.</strong>
