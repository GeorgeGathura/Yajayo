                <h3>The Big Question</h3>
                <p>Does a producer or manufacturer have any responsibility for where their product ends up 
                    once it leaves their premises?</p>
                <p>This continues to be a heated debate among environmentalists, government bodies, and manufacturers. 
                    At the recently concluded UN-backed Intergovernmental Negotiating Committee (INC-5.2) on plastic
                    pollution in Geneva (August 2025), members once again failed to agree on the right course of action 
                    to tackle the growing waste menace caused by packaging material such as plastic.</p>

               <h3> The Debate: Ban or Recycle?</h3>
                The main action points under discussion were:
                <ul>
                    <li>Ban or significantly reduce the use of certain types of plastic packaging.</li>
                    <li>Improve recycling initiatives to manage existing waste.</li>
                </ul>
                <p> Manufacturers argue that bans are unrealistic. Alternatives to plastic often cost more 
                    and exploit natural resources:</p>
                <ul>
                    <li>Shifting to paper packaging means cutting down more trees.</li>
                    <li>Moving to glass packaging increases sand harvesting.</li>
                </ul>
                
                

                <p>Environmentalists and governments counter that recycling simply ‚Äúkicks the can down the road,‚Äù pushing the cost and burden of reclamation onto taxpayers. Worse still, poorly designed products such as multi-layer plastics are almost impossible to recycle effectively.</p>
                <strong>Kenya‚Äôs Response: From Bans to Shared Responsibility</strong>
                <p>Kenya has been proactive:</p>
                <ul>
                    <li>2017: Ban on plastic carrier bags.</li>
                    <li>2020: Ban on single-use plastics in protected green spaces.</li>
                </ul>
                
                

                <p>Fearing further bans, manufacturers‚Äîthrough the Kenya Association of Manufacturers‚Äîpushed for Extended Producer Responsibility (EPR) to be embedded in law. 
                    This came to life through the Sustainable Waste Management Act of 2022, which compels producers to participate in waste management.</p>

                <p>Actions under EPR include:</p>
               <p> <ul>
                    <li>Take-back schemes</li>
                    <li>Responsible manufacturing practices</li>
                    <li>Waste production declarations</li>
                    <li>Paying levies to fund awareness and waste management programs</li>
                </ul></p>
               
